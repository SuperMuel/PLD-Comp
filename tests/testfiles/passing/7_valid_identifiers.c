int main(){
    /* start with a _ */
    int _a;

    /* _ is allowed in the middle */
    int a_b;

    /* end with a _ */
    int a_;

    /* allow numbers */
    int a0;
    int a9;

    /* allow letters */
    int aA;
    int aZ;

    /* Start with capital letter */
    int A;
    int Z;

    /* Start with lowercase letter */
    int a;
    int z;


    /* Use the variables to suppress warnings*/
    _a = 0;
    a_b = 0;
    a_ = 0;
    a0 = 0;
    a9 = 0;
    aA = 0;
    aZ = 0;
    A = 0;
    Z = 0;
    a = 0;
    z = 0;


    return 0;
}